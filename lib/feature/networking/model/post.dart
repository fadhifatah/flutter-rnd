class Post {
  final int userId;
  final int id;
  final String title;
  final String body;

  const Post({
    required this.userId,
    required this.id,
    required this.title,
    required this.body,
  });

  factory Post.fromJson(Map<String, dynamic> json) {
    return Post(
      userId: json['userId'],
      id: json['id'],
      title: json['title'],
      body: json['body'],
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'userId': userId,
      'id': id,
      'title': title,
      'body': body,
    };
  }
}

class ListPost {
  final List<Post> dataList;

  const ListPost({required this.dataList});

  factory ListPost.fromJson(List<dynamic> json) {
    return ListPost(dataList: json.map((e) => Post.fromJson(e)).toList());
  }
}
